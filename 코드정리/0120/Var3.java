
public class Var3 {

	public static void main(String[] args) {
		int i = 'A';
		int i2 = 'B';
		int i3 = 'a';
		int i4 = 'b';
		System.out.println(i);
		System.out.println(i2);
		System.out.println(i3);
		System.out.println(i4);
		int k = (int) 3.14;
		System.out.println(k);
		char ch = (char) 65;
		System.out.println(ch);
	}

}
