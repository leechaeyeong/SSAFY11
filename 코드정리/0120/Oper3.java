
public class Oper3 {

	public static void main(String[] args) {
		System.out.println(!(1 > 1));
		System.out.println(1 < 1);
		System.out.println(1 >= 1);
		System.out.println(1 <= 1);
		System.out.println(1 == 1);
		System.out.println(1 != 1);
		String s1 = new String("hi");
		String s2 = new String("hi");
		System.out.println(s1 == s2);
		System.out.println(s1 != s2);
		String s3 = "hi";
		String s4 = "hi";
		System.out.println(s3 == s4);
		System.out.println(s3 != s4);
	}

}
